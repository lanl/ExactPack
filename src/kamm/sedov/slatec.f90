      FUNCTION r1mach(i)
c
c This function is intended to replace the old R1MACH by using F90
c intrinsic functions. 
c
c The traditional R1MACH constants are ... 
c
c -- SINGLE-PRECISION MACHINE CONSTANTS -- 
c R1MACH(1) = B**(EMIN-1), THE SMALLEST POSITIVE MAGNITUDE.
c R1MACH(2) = B**EMAX*(1 - B**(-T)), THE LARGEST MAGNITUDE.
c R1MACH(3) = B**(-T), THE SMALLEST RELATIVE SPACING.
c R1MACH(4) = B**(1-T), THE LARGEST RELATIVE SPACING.
c R1MACH(5) = LOG10(B)
c
!-------------------------------------------------------------------
c  .. Implicit None Statement ..
      IMPLICIT NONE
c  .. Function Return Value ..
      REAL :: r1mach
c  .. Scalar Arguments ..
      INTEGER :: i
c  .. Local Scalars ..
      LOGICAL, SAVE :: qfirst_call = .TRUE.
c  .. Local Arrays ..
      REAL, SAVE :: r1mach_values(5)
c  .. Intrinsic Functions ..
      INTRINSIC digits, epsilon, huge, log10, radix, real, tiny
c  .. Executable Statements .. 
      IF (i<1 .OR. i>5) THEN
        WRITE (*,'(1x,''R1MACH(I) - I out of bounds, I ='',i10)') i
        STOP ' R1MACH(I) - I out of bounds'
      END IF
      IF (qfirst_call) THEN
        r1mach_values = (/ tiny(1.0E0), huge(1.0E0),
     &    real(radix(1.0E0))**(-digits(1.0E0)),
     &    epsilon(1.0E0), log10(real(radix(1.0E0))) /)
        qfirst_call = .FALSE.
      END IF
      r1mach = r1mach_values(i)
      RETURN
      END FUNCTION r1mach 

